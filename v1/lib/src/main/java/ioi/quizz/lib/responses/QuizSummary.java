package ioi.quizz.lib.responses;

public class QuizSummary {
    
    private int total;
    
    private int correct;
    
    public int getTotal() {
        return total;
    }
    
    public void setTotal(int total) {
        this.total = total;
    }
    
    public int getCorrect() {
        return correct;
    }
    
    public void setCorrect(int correct) {
        this.correct = correct;
    }
}
